				<div class="section_heading">TradeHill</div>
				<br>
				Originally launched in May 2011. TradeHill is formerly the largest US based BitCoin exchange and 2nd largest exchange globally.<br>
				<br>
				TradeHill is currently undergoing reorganization and will be relaunching with additional services for the BitCoin and digital currency communitites. The primary focus is ease of use and expandign access to digital currencies.<br>
