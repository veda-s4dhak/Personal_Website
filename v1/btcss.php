				<div class="section_heading">Ethereum</div>
				<br>
				Ethereum is a public blockchain platform with programmable transaction functionality. It provides a decentralized virtual machine that can execute peer-to-peer contracts using a crypto asset called Ether (unofficial code ETH).<br>
				<br>
				The Ethereum system has been described as "a single shard computer that is run by the network of users and on which resources are parceled out and paid for by Ether."<br>
				<br>
				Important people: Vitalik Buterin, Gavin Wood, Jeffrey Wilcke<br>
				<br>
				Source: https://en.wikipedia.org/wiki/Ethereum<br>
				<br>

				<div class="section_heading">BitPagos</div>
				<br>
				BitPagos is a payment gateway that supports bitcoins and is focused in Latin America. The gateway accepts bitcoin and credit card payments, pays out to merchants in local currency or bitcoins. BitPagos is operational, but currently working in private beta with a few merchants.<br>
				<br>
				Important people: Sebastian Serrano, Luciana Gruszeczka and Mugur Marculescu<br>
				<br>
				Source: https://www.crunchbase.com/organization/bitpagos#/entity<br>
				<br>

				<div class="section_heading">ButterCoin</div>
				<br>
				ButterCoin was a private American startup company that provided digital currency exchange services to the US. Buttercoin operated a full order book trading platform for buying and selling bitcoins.<br>
				<br>
				After loss of interest from VC firms in April 6th, 2015, the company announced it was shutting down<br>
				<br>
				Important people: Cedric Dahl, Bennett Hoffman, Kevin Zhou, Kevin Adams<br>
				<br>
				Source: https://en.wikipedia.org/wiki/Buttercoin<br>
				<br>

				<div class="section_heading">Gliph</div>
				<br>
				Gliph offers primarily three services:
				<ul>
					<li>Message with people in groups or privately</li>
					<li>Offers a bitcoin payment system</li>
					<li>Has an online marketplace systems where users can advertise or purchase products with BitCoin</li>
				</ul>
				Gliph has a mobile app and a desktop app.<br>
				<br>
				Important people: Rob Banagale<br>
				<br>
				Source: https://www.crunchbase.com/organization/gliph#/entity<br>
				<br>

				<div class="section_heading">CoinBase</div>
				<br>
				Coinbase is a bitcon wallet and exchange company headquartered in San Francisco, California. It operates exchanges between BitCoin & Fiat Currencies in 32 countries and BitCoin transactions & storage in 190 countries worldwide.<br>
				<br>
				Coinbase has three core products: an exchange for trading bitcoin and fiat currency, a wallet for BitCoin storage and transactions, and an API for developers and merchants to build applications and accept BitCoin payments.<br>
				<br>
				The exchange can be funded through a bank transfer or wire, and trades on the exchange have a maker/taker price model in which traders either a 0.25% free (taker) or nothing (maker) to execute trades.<br>
				<br>
				Important people: Brian Armstrong, Fred Ehrsam<br>
				<br>
				Source: https://en.wikipedia.org/wiki/Coinbase<br>
				<br>

				<div class="section_heading">VerifyBTC</div>
				<br>
				Allows identity verification in BitCoin. It provides frictionless authentication, offering an extra layer of security of BitCoin transactions.<br>
				<br>
				Enterprise Software currently under intense development. Demo will be on Sep 19th at Boost VC.<br>
				<br>
				Important people: Gabriel Gomez Rojo, Hongwei Li, Manoj Das<br>
				<br>
				Source: https://en.wikipedia.org/wiki/Coinbase<br>
				<br>

				<div class="section_heading">BTCJam</div>
				<br>
				BTCJam is a company is San Francisco. It provides a peer-to-peer lending service where people from around the world connect to borrow and lend using BitCoin. It is allowed to users in countries that lack a local credit score system to receive loans, based on an in-house credit scoring algorithim. Its mission is to make credit affordable and accessible anywhere.<br>
				<br>
				Important people: Celso Pitta, Gustavo Guida Reis, Charles Moldow, Micky Malka<br>
				<br>
				Source: https://en.wikipedia.org/wiki/BTCJam<br>
				<br>

				<div class="section_heading">Circle</div>
				<br>
				Circle is a peer-to-peer payments technology company utilizing BitCoin and traditional fiat currencies. Circle's mobile payment platform, Circle Pay, allows users to hold, send and receive traditional fiat currencies similiar to Venmo. Circle Pay can also operate as a bitcoin wallet service to buy and sell bitcoins.<br>
				<br>
				Important people: Jeremy Allaire, Sean Neville<br>
				<br>
				Source: https://en.wikipedia.org/wiki/Circle_(company)<br>
