				<div class="section_heading">Ripple</div>
				<br>
				Ripple is a real-time gross settlement system (RTGS), currency exchange and remittance network by Ripple Labs. Also called the Ripple Transactiona Protocol (RTXP) or Ripple protocol, it is built upon a distributed open source Internet protocol, consenses ledger and native currency called XRP (ripples). Released in 2012, Ripple purports to enable "secure, instant and nearly free global financial transactions of any size with no chargebacks." It supports tokens representing fiat currency, cryptocurrency, commodity or any other unit of value such as frequent flyer miles or mobile minutes. At its core, Ripple is based around a shared, public database or ledger, which uses a consensus process that allows for payments, exchanges and remittance ina distributed process. The security of the Ripple consensus algorithim was challenged in 2014, with Ripple defending the safety of the system. As of 2016, Ripple is the third-largest cryptocurrency by market capitalization, after BitCion and Ethereum. Currently implemented by companies such as Fidor Bank, the Ripple protocol has been increasingly adopted by banks and payment networks as settlement infrastructure technology, with American Banker explainging that "from banks' perspective, distributed ledgers like the Ripple system have a number of advantages over cryptocurrencies like BitCoin," including price and security.<br>
				<br>
				Important people: Arthur Britto, David Schwartz, Ryan Fugger<br>
				<br>
				Source: https://en.wikipedia.org/wiki/Ripple_(payment_protocol)<br>
